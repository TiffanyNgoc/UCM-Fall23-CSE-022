#include<iostream>
using namespace std;

int main() {

    cout << ".--------------------------------------------------------." << endl;
    cout << "|                                                        |" << endl;
    cout << "| Sonnet 120: That You Were Once Unkind Befriends Me Now |" << endl;
    cout << "|                                                        |" << endl;
    cout << "| ------------------------------------------------------ |" << endl;
    cout << "|                                                        |" << endl;
    cout << "| That you were once unkind befriends me now,            |" << endl;
    cout << "| And for that sorrow, which I then did feel,            |" << endl;
    cout << "| Needs must I under my transgression bow,               |" << endl;
    cout << "| Unless my nerves were brass or hammer'd steel.         |" << endl;
    cout << "|                                                        |" << endl;
    cout << "| For if you were by my unkindness shaken,               |" << endl;
    cout << "| As I by yours, you've passed a hell of time;           |" << endl;
    cout << "| And I, a tyrant, have no leisure taken                 |" << endl;
    cout << "| To weigh how once I suffered in your crime.            |" << endl;
    cout << "|                                                        |" << endl;
    cout << "| O! that our night of woe might have remembered         |" << endl;
    cout << "| My deepest sense, how hard true sorrow hits,           |" << endl;
    cout << "| And soon to you, as you to me, then tendered           |" << endl;
    cout << "| The humble salve, which wounded bosoms fits!           |" << endl;
    cout << "|                                                        |" << endl;
    cout << "|     But that your trespass now becomes a fee;          |" << endl;
    cout << "|     Mine ransoms yours, and yours must ransom me.      |" << endl;
    cout << "|                                                        |" << endl;
    cout << "| ------------------------------------------------------ |" << endl;
    cout << "|                                  - William Shakespeare |" << endl;
    cout << ".--------------------------------------------------------." << endl;

    return 0;

}